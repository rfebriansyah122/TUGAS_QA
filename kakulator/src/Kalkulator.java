

class Kalkulator {
    public static int tambah(int num1, int num2) {
        return num1 + num2;
    }

    public static int kurang(int num1, int num2) {
        return num1 - num2;
    }

    public static int kali(int num1, int num2) {
        return num1 * num2;
    }

    public static int bagi(int num1, int num2) {
        return num1 / num2;
    }
}